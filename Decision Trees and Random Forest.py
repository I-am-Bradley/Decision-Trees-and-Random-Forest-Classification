# -----------------------------------------------------------------------------
# Original pixel values: 0 -> 255
# If we allow all 256 values:
#   Each decision node could create 256 branches
# This becomes:
#   - extremely slow
#   - memory expensive
#   - prone to overfitting
# Instead:
#   pixel < threshold  -> 0
#   pixel >= threshold -> 1
# This dramatically simplifies the tree.
# -----------------------------------------------------------------------------

def binarize_images(images, threshold=128):

    return (images >= threshold).astype(np.uint8)

# -----------------------------------------------------------------------------
# FLATTEN IMAGES
# Trees expect tabular data:
# BEFORE: (60000, 28, 28)  AFTER: (60000, 784)
# Each image becomes one row and each pixel becomes one feature.
# -----------------------------------------------------------------------------

def flatten_images(images):

    return images.reshape(images.shape[0], -1)

# -----------------------------------------------------------------------------
# Convert continuous numeric features into discrete bins.
# This is useful for the extra dataset because C4.5 is naturally designed
# for categorical/discrete values.
# Example:
#        A continuous feature like radius_mean can be converted into values:
#            0, 1, 2, 3
#   The code uses percentiles so each bin has a reasonable number of examples.
# -----------------------------------------------------------------------------

def discretize_numeric_features(X, bins=4):
    

    X_discrete = np.zeros_like(X, dtype=int)

    for col in range(X.shape[1]):
        values = X[:, col]

        # Create percentile cut points.
        # Example for bins=4:
        #   25th percentile, 50th percentile, 75th percentile
        cut_points = np.percentile(values, np.linspace(0, 100, bins + 1)[1:-1])

        # np.digitize assigns each value to a bin.
        X_discrete[:, col] = np.digitize(values, cut_points)

    return X_discrete


