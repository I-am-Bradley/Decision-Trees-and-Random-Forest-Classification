def split_data(X, y, train_ratio=0.8):
    np.random.seed(1)
    indices = np.arange(len(X))
    np.random.shuffle(indices)

    X = X[indices]
    y = y[indices]

    split_index = int(train_ratio * len(X))

    return X[:split_index], y[:split_index], X[split_index:], y[split_index:]


def run_and_print(name, model, X_train, y_train, X_test, y_test, num_classes):
    print("\n==============================")
    print(name)
    print("==============================")

    start = time.time()

    model.fit(X_train, y_train)
    predictions = model.predict(X_test)

    end = time.time()

    print(f"Accuracy: {accuracy(y_test, predictions):.4f}")
    print(f"Runtime: {end - start:.2f} seconds")
    print("Confusion Matrix:")
    print(confusion_matrix(y_test, predictions, num_classes))

    return accuracy(y_test, predictions), end - start


def run_adaboost_binary_test(name, X_train, y_train, X_test, y_test, n_estimators):
    print("\n==============================")
    print(name)
    print("==============================")

    start = time.time()

    y_train_binary = np.where(y_train == 1, 1, -1)

    model = AdaBoostBinary(n_estimators=n_estimators)
    model.fit(X_train, y_train_binary)

    pred_binary = model.predict(X_test)
    predictions = np.where(pred_binary == 1, 1, 0)

    end = time.time()

    print(f"Accuracy: {accuracy(y_test, predictions):.4f}")
    print(f"Runtime: {end - start:.2f} seconds")
    print("Confusion Matrix:")
    print(confusion_matrix(y_test, predictions, 2))

    return accuracy(y_test, predictions), end - start


