# SECTION 3 - ENTROPY / INFORMATION GAIN
# =============================================================================

# -----------------------------------------------------------------------------
# ENTROPY
# Entropy measures impurity/disorder.
# Formula:  H(S) = - Σ p(x) log2 p(x)
# Lower entropy:  More pure
# Higher entropy:  More mixed
# Examples:
# Pure node:
#   [all class 3]
#   entropy = 0
# Mixed node:
#   [many different classes]
#   entropy = high
# -----------------------------------------------------------------------------

def entropy(y):

    # Count occurrences of each class
    classes, counts = np.unique(y, return_counts=True)

    # Convert counts into probabilities
    probabilities = counts / len(y)

    # Compute entropy
    ent = -np.sum(probabilities * np.log2(probabilities))

    # Handle NaNs
    ent = np.nan_to_num(ent, nan=0.0)

    return ent

# -----------------------------------------------------------------------------
# SPLIT INFO
# Used in C4.5 gain ratio formula.
# Formula:  SplitInfo = - Σ (|Si| / |S|) log2(|Si| / |S|)
# This penalizes splits that create too many tiny branches.
# -----------------------------------------------------------------------------

def split_info(subsets, total_size):

    values = []

    for subset in subsets:
        proportion = len(subset) / total_size

        if proportion > 0:
            values.append(-proportion * np.log2(proportion))

    return np.sum(values)

# -----------------------------------------------------------------------------
# GAIN RATIO
# C4.5 uses: GainRatio = InformationGain / SplitInfo
# If SplitInfo = 0:
#   GainRatio = 0
# -----------------------------------------------------------------------------

def information_gain_and_ratio(X_column, y):
    """Return information gain, split information, and gain ratio.

    C4.5 does not select the attribute with the highest gain ratio from all
    available attributes. It first calculates information gain for every
    candidate attribute, computes the average information gain, discards
    attributes below that average, and only then chooses the highest gain
    ratio from the remaining attributes.
    """
    total_entropy = entropy(y)
    values = np.unique(X_column)

    weighted_entropy = 0.0
    subsets = []

    for value in values:
        subset_y = y[X_column == value]
        subsets.append(subset_y)

        weight = len(subset_y) / len(y)
        weighted_entropy += weight * entropy(subset_y)

    information_gain = total_entropy - weighted_entropy
    splitinfo = split_info(subsets, len(y))

    # C4.5 special case: gain ratio is zero when split information is zero.
    if splitinfo == 0.0:
        gainratio = 0.0
    else:
        gainratio = information_gain / splitinfo

    return information_gain, splitinfo, gainratio


def information_gain(X_column, y):
    """Compute information gain for one candidate attribute."""
    gain, _, _ = information_gain_and_ratio(X_column, y)
    return gain


def gain_ratio(X_column, y):
    """Compute gain ratio for one candidate attribute."""
    _, _, ratio = information_gain_and_ratio(X_column, y)
    return ratio

