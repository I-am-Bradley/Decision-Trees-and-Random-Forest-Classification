# SECTION 6 - RANDOM FOREST
# Random Forest classifier made from many C4.5 decision trees.
#   Randomness comes from:
#       1. Bootstrap sampling
#       2. Random feature subsets at each split
# =============================================================================

class RandomForest:

    def __init__(self,
                 n_trees=25,
                 max_depth=15,
                 min_samples_split=5,
                 random_features=50,
                 bootstrap_size=None):

        self.n_trees = n_trees
        self.max_depth = max_depth
        self.min_samples_split = min_samples_split
        self.random_features = random_features
        self.bootstrap_size = bootstrap_size
        self.trees = []

    # -------------------------------------------------------------------------
    # BOOTSTRAP SAMPLE
    # Randomly sample WITH replacement.
    # Some examples repeat.
    # Some are omitted.
    # This introduces randomness.
    # -------------------------------------------------------------------------

    def bootstrap_sample(self, X, y):

        n_samples = len(X)
        size = self.bootstrap_size or n_samples

        indices = np.random.choice(
            n_samples,
            size,
            replace=True
        )

        return X[indices], y[indices]

    # -------------------------------------------------------------------------
    # TRAIN FOREST
    # -------------------------------------------------------------------------

    def fit(self, X, y):

        self.trees = []

        for i in range(self.n_trees):
            print(f"Training tree {i+1}/{self.n_trees}")

            sample_X, sample_y = self.bootstrap_sample(X, y)

            tree = C45DecisionTree(
                max_depth=self.max_depth,
                min_samples_split=self.min_samples_split,
                random_features=self.random_features
            )

            tree.fit(sample_X, sample_y)
            self.trees.append(tree)

    # -------------------------------------------------------------------------
    # PREDICT USING MAJORITY VOTING
    # Every tree votes.
    #
    # Final prediction:
    #   class with most votes
    # -------------------------------------------------------------------------

    def predict(self, X):

        tree_predictions = []

        for tree in self.trees:
            tree_predictions.append(tree.predict(X))

        tree_predictions = np.array(tree_predictions)

        final_predictions = []

        for i in range(X.shape[0]):
            votes = tree_predictions[:, i]
            values, counts = np.unique(votes, return_counts=True)
            final_predictions.append(values[np.argmax(counts)])

        return np.array(final_predictions)


