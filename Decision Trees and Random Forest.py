# SECTION 4 - DECISION TREE NODE
# =============================================================================

# -----------------------------------------------------------------------------
# TREE NODE
# Each node stores:
# feature: Which attribute/pixel we split on
# children: Dictionary mapping feature values to child nodes
# prediction: Majority class at this node
# is_leaf: True if node is terminal
# -----------------------------------------------------------------------------

class TreeNode:

    def __init__(self):
        self.feature = None
        self.children = {}
        self.prediction = None
        self.is_leaf = False

# =============================================================================
# SECTION 5 - C4.5 DECISION TREE
#  For a normal single decision tree:
#       random_features = None
#   For random forest trees:
#       random_features = number of random features to consider at each split
# =============================================================================

class C45DecisionTree:

    def __init__(self,
                 max_depth=15,
                 min_samples_split=5,
                 random_features=None):

        self.max_depth = max_depth
        self.min_samples_split = min_samples_split
        self.random_features = random_features
        self.root = None

    # -------------------------------------------------------------------------
    # TRAIN TREE
    # -------------------------------------------------------------------------

    def fit(self, X, y):

        features = np.arange(X.shape[1])
        self.root = self.build_tree(X, y, features, depth=0)

    # -------------------------------------------------------------------------
    # BUILD TREE RECURSIVELY
    # The tree recursively splits the dataset.
    # Stopping conditions:
    #   1. All labels identical
    #   2. No features left
    #   3. Max depth reached
    #   4. Too few examples
    # -------------------------------------------------------------------------

    def build_tree(self, X, y, features, depth):

        node = TreeNode()

        # Majority class prediction
        node.prediction = self.majority_class(y)

        # STOP CONDITION 1: if all examples have the same class.
        if len(np.unique(y)) == 1:
            node.is_leaf = True
            return node

        # STOP CONDITION 2: if there are no features left.
        if len(features) == 0:
            node.is_leaf = True
            return node

        # STOP CONDITION 3: if max depth is reached.
        if depth >= self.max_depth:
            node.is_leaf = True
            return node

        # STOP CONDITION 4: if too few samples are available.
        if len(y) < self.min_samples_split:
            node.is_leaf = True
            return node

        # ---------------------------------------------------------------------
        # RANDOM FEATURE SUBSET
        # Used ONLY for Random Forest
        # ---------------------------------------------------------------------

        candidate_features = features

        if self.random_features is not None:
            subset_size = min(self.random_features, len(features))
            candidate_features = np.random.choice(
                features,
                subset_size,
                replace=False
            )

        # ---------------------------------------------------------------------
        # FIND BEST FEATURE
        # ---------------------------------------------------------------------

        # C4.5 attribute selection is a two-stage process:
        #   1. Calculate information gain for every candidate feature.
        #   2. Calculate the average information gain and eliminate features
        #      whose information gain is below that average.
        #   3. Among the remaining features, select the feature with the
        #      highest gain ratio.
        #
        # This filter prevents an attribute with very small information gain
        # and very small split information from receiving an artificially high
        # gain ratio.
        feature_statistics = []

        for feature in candidate_features:
            info_gain, splitinfo, ratio = information_gain_and_ratio(
                X[:, feature], y
            )
            feature_statistics.append(
                (feature, info_gain, splitinfo, ratio)
            )

        positive_gains = [
            info_gain
            for _, info_gain, _, _ in feature_statistics
            if info_gain > 0.0
        ]

        # If every candidate has zero information gain, no useful split exists.
        if not positive_gains:
            node.is_leaf = True
            return node

        average_information_gain = float(np.mean(positive_gains))

        eligible_features = [
            (feature, info_gain, ratio)
            for feature, info_gain, _, ratio in feature_statistics
            if info_gain >= average_information_gain and ratio > 0.0
        ]

        # Numerical edge cases can leave no eligible feature. Falling back to
        # the largest positive information gain is safer than creating an
        # invalid split and still preserves deterministic behavior.
        if eligible_features:
            best_feature, best_info_gain, best_gain_ratio = max(
                eligible_features,
                key=lambda item: (item[2], item[1], -int(item[0]))
            )
        else:
            fallback = [
                (feature, info_gain, ratio)
                for feature, info_gain, _, ratio in feature_statistics
                if info_gain > 0.0
            ]
            best_feature, best_info_gain, best_gain_ratio = max(
                fallback,
                key=lambda item: (item[1], item[2], -int(item[0]))
            )

        if best_feature is None or best_info_gain <= 0.0:
            node.is_leaf = True
            return node

        node.feature = best_feature

        # ---------------------------------------------------------------------
        # SPLIT DATA
        # ---------------------------------------------------------------------

        values = np.unique(X[:, best_feature])
        remaining_features = features[features != best_feature]

        for value in values:
            mask = X[:, best_feature] == value

            child_X = X[mask]
            child_y = y[mask]

            child_node = self.build_tree(
                child_X,
                child_y,
                remaining_features,
                depth + 1
            )

            node.children[value] = child_node

        return node

    # -------------------------------------------------------------------------
    # MAJORITY CLASS
    # Return the most common class label.
    # -------------------------------------------------------------------------

    def majority_class(self, y):

        values, counts = np.unique(y, return_counts=True)
        return values[np.argmax(counts)]

    # -------------------------------------------------------------------------
    # PREDICT SINGLE EXAMPLE
    # Predict one example by walking through the tree.
    # -------------------------------------------------------------------------

    def predict_one(self, x, node):

        # If leaf:
        # return stored prediction
        if node.is_leaf:
            return node.prediction

        feature_value = x[node.feature]

        # If branch exists:
        if feature_value in node.children:
            return self.predict_one(
                x,
                node.children[feature_value]
            )

        # If unseen branch:
        return node.prediction

    # -------------------------------------------------------------------------
    # PREDICT MULTIPLE EXAMPLES
    # -------------------------------------------------------------------------

    def predict(self, X):

        predictions = []

        for x in X:
            predictions.append(
                self.predict_one(x, self.root)
            )

        return np.array(predictions)


