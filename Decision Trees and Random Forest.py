class Tee:
    """
    Write output to multiple streams at the same time.

    This allows all existing print() statements to continue appearing in the
    terminal while also being saved to a text file.
    """

    def __init__(self, *streams):
        self.streams = streams

    def write(self, text):
        for stream in self.streams:
            stream.write(text)
            stream.flush()

        return len(text)

    def flush(self):
        for stream in self.streams:
            stream.flush()

    def isatty(self):
        return any(
            getattr(stream, "isatty", lambda: False)()
            for stream in self.streams
        )


def save_print_output(filename):
    """
    Decorator that saves every print() generated by a function and any
    functions it calls.

    Output is written to both:
      1. The terminal
      2. The specified text file

    The output file is overwritten each time the decorated function runs.
    """

    def decorator(function):
        @functools.wraps(function)
        def wrapper(*args, **kwargs):
            original_stdout = sys.stdout

            with open(filename, "w", encoding="utf-8") as log_file:
                sys.stdout = Tee(original_stdout, log_file)

                try:
                    print("=" * 80)
                    print(f"Saved output for: {function.__name__}")
                    print(f"Output file: {filename}")
                    print("=" * 80)
                    print()

                    return function(*args, **kwargs)

                finally:
                    print()
                    print("=" * 80)
                    print(f"End of saved output for: {function.__name__}")
                    print("=" * 80)

                    sys.stdout = original_stdout

        return wrapper

    return decorator


