#
#   Load the Kaggle Breast Cancer Wisconsin dataset as the extra dataset.
#
#   Expected file:
#       data.csv
#
#   Standard Kaggle columns:
#       column 0 = id
#       column 1 = diagnosis
#       columns 2 through second-to-last = numeric tumor features
#       final column = usually empty, named Unnamed: 32
#
#   Prediction target:
#       diagnosis
#   
#   Encoding:
#       M = malignant = 1
#       B = benign = 0
# =============================================================================
def load_extra_dataset(filename):
    raw = np.genfromtxt(filename, delimiter=",", dtype=str, skip_header=1)

    # Column 1 is the label we are predicting.
    y_text = raw[:, 1]
    # Convert labels to numbers.
    y = np.where(y_text == "M", 1, 0)

    # Use columns 2 through the second-to-last column as features.
    # This removes:
    #   id
    #   diagnosis
    X = raw[:, 2:]

    if X[0, -1] == "" or X[0, -1].lower() == "nan":
        X = X[:, :-1]

    X = X.astype(float)

    return X, y

