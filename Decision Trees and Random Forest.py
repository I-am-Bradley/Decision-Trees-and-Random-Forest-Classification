# -----------------------------------------------------------------------------
# Decision stump:
# very weak learner
# AdaBoost combines many weak learners into one strong classifier.
# -----------------------------------------------------------------------------

class DecisionStump:

    def __init__(self):
        self.feature = None
        self.threshold = None
        self.polarity = 1
        self.alpha = 0

    def predict(self, X):

        n_samples = X.shape[0]
        predictions = np.ones(n_samples)

        if self.polarity == 1:
            predictions[X[:, self.feature] < self.threshold] = -1
        else:
            predictions[X[:, self.feature] >= self.threshold] = -1

        return predictions


# -----------------------------------------------------------------------------
# AdaBoost Binary Classifier
# -----------------------------------------------------------------------------

class AdaBoostBinary:

    def __init__(self, n_estimators=25):
        self.n_estimators = n_estimators
        self.stumps = []

    # Train AdaBoost.

    # Each round:
    #    1. Find best weak stump
    #    2. Compute stump weight alpha
    #    3. Increase weights of misclassified examples
    #    4. Decrease weights of correctly classified examples

    def fit(self, X, y):

        n_samples, n_features = X.shape

        # Initialize equal weights
        weights = np.full(n_samples, 1 / n_samples)
        self.stumps = []

        for estimator in range(self.n_estimators):
            print(f"Training AdaBoost stump {estimator + 1}/{self.n_estimators}")

            stump = DecisionStump()
            min_error = float('inf')

            # Search all features
            for feature in range(n_features):
                feature_values = np.unique(X[:, feature])

                for threshold in feature_values:
                    predictions = np.ones(n_samples)
                    predictions[X[:, feature] < threshold] = -1

                    # Weighted error
                    error = np.sum(
                        weights[y != predictions]
                    )
                    polarity = 1

                    # Flip if worse than random
                    if error > 0.5:
                        error = 1 - error
                        polarity = -1

                    if error < min_error:
                        stump.feature = feature
                        stump.threshold = threshold
                        stump.polarity = polarity
                        min_error = error

            EPS = 1e-10

            # Alpha controls influence of stump
            stump.alpha = 0.5 * np.log(
                (1 - min_error + EPS) /
                (min_error + EPS)
            )

            predictions = stump.predict(X)

            # Update weights
            weights *= np.exp(
                -stump.alpha * y * predictions
            )

            # Normalize
            weights /= np.sum(weights)
        
            self.stumps.append(stump)

    # Return raw AdaBoost confidence scores.
    def predict_scores(self, X):

        scores = np.zeros(X.shape[0])

        for stump in self.stumps:
            scores += stump.alpha * stump.predict(X)

        return scores
    
    # Convert scores into class labels.
    def predict(self, X):
        
        scores = self.predict_scores(X)
        return np.where(scores >= 0, 1, -1)



# -----------------------------------------------------------------------------
# ONE VS REST ADABOOST
# Since MNIST has 10 classes: 0-9
# We train: 10 binary classifiers
# Example:
#   Model 0:
#       0 vs not-0
#   Model 1:
#       1 vs not-1
# -----------------------------------------------------------------------------

class AdaBoostOVR:

    def __init__(self, n_estimators=25):
        self.n_estimators = n_estimators
        self.models = {}

    # Train one binary AdaBoost model for each class.
    def fit(self, X, y):

        self.models = {}
        classes = np.unique(y)

        for cls in classes:

            print(f"Training AdaBoost for class {cls}")

            binary_y = np.where(y == cls, 1, -1)

            model = AdaBoostBinary(
                n_estimators=self.n_estimators
            )

            model.fit(X, binary_y)
            self.models[cls] = model

    #  Choose the class with the highest AdaBoost score.
    def predict(self, X):

        scores = []
        classes = sorted(self.models.keys())

        for cls in classes:
            model_scores = self.models[cls].predict_scores(X)
            scores.append(model_scores)

        scores = np.array(scores)
        predictions = np.argmax(scores, axis=0)

        return predictions


